#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*src;
	size_t	i;

	i = 0;
	src = (char *)s;
	while (*src != '\0')
	{
		if (*src == (char)c)
			return (src);
		src++;
	}
	if (*src == (char)c && (char)c == '\0')
		return (src);
	return (NULL);
}
